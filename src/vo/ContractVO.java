package vo;

import java.sql.Date;

public class ContractVO {
	private int contract_id;
	private int contract_period;
	private Date contract_moving_in;
	private Date contract_date;
	private int room_id;
	private String mem_id;
	private int realtor_id;
	public int getContract_id() {
		return contract_id;
	}
	public void setContract_id(int contract_id) {
		this.contract_id = contract_id;
	}
	public int getContract_period() {
		return contract_period;
	}
	public void setContract_period(int contract_period) {
		this.contract_period = contract_period;
	}
	public Date getContract_moving_in() {
		return contract_moving_in;
	}
	public void setContract_moving_in(Date contract_moving_in) {
		this.contract_moving_in = contract_moving_in;
	}
	public Date getContract_date() {
		return contract_date;
	}
	public void setContract_date(Date contract_date) {
		this.contract_date = contract_date;
	}
	public int getRoom_id() {
		return room_id;
	}
	public void setRoom_id(int room_id) {
		this.room_id = room_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public int getRealtor_id() {
		return realtor_id;
	}
	public void setRealtor_id(int realtor_id) {
		this.realtor_id = realtor_id;
	}
	
}
